package wj.csv.pojo;


public class TableList {
	private String tableName;

	public String getTableName() {
		return tableName;
	}

	public void setTableName(String tableName) {
		this.tableName = tableName;
	}

	@Override
	public String toString() {
		return "TableList [tableName=" + tableName + "]";
	}
}
